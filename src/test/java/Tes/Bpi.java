package Tes;

public class Bpi {
	private String id;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public float getPpu() {
		return ppu;
	}
	public void setPpu(float ppu) {
		this.ppu = ppu;
	}
	private String type;
	private String name;
	private float ppu;

}
